const color = 'red';
switch (color) {
    case 'green':
        console.log('This green color');
        
        break;
    case 'red':
        console.log('This red color');
        
        break;
    case 'yellow':
        console.log('This yellow color');
        
        break;
    case 'black':
        console.log('This black color');
        
        break;
    default:
        console.log('No match');
}